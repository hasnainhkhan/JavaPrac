class AreaPerimeter{
    public static void main(String [] args){
        double height , width,area,perimeter;
        height = 5.5d;
        width = 8.5d;
        area = height * width ;
        perimeter = 2 * (height + width);

        System.out.println("Area:"+area + "Perimeter::" +perimeter);
    }

}
